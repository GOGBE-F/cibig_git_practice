
## afficher le contenu de la liste avec while

#creer la liste
animal_list = ['cow', 'mousse', 'yeast', 'bacteria']

## parcourir la liste avec while
# la taille de la liste avec len

nb_list = len(animal_list)

## la condition de la boucle pour le while
i = 0
while i <= nb_list :
    print(i)
    i = i + 1

## la condition pour le for
## creer une variable indice
## indice = i

for animal in animal_list:
    print(animal)




